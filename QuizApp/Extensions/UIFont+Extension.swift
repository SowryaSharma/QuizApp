import Foundation
import UIKit

extension UIFont {
    static var titleFont: UIFont {
        return UIFont.systemFont(ofSize: 20, weight: .bold)
    }
    static var subTitleFont: UIFont {
        return UIFont.systemFont(ofSize: 18 , weight: .bold)
    }
}
